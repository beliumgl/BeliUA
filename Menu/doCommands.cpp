#include <iostream>

void Calc();
int convert();
int secret();
int writeMenu();
int clearScreen();
int autoclicker();

int doCommands(std::string option) {
	if (option == "1") {				// This `if` is statements are checking what have user chosen and run needed script
		Calc();
	}
	else if (option == "2") {
		convert();
	}
	else if (option == "3") {
		autoclicker();
	}
	else if (option == "4") {
		secret();
	}
	else {								// This "restarts" the program if user have chosen wrong script
		clearScreen();
		writeMenu();
	}
	return 0;
}